package stratery.bt2;

public class HangHoa {
    private String tenHangHoa;
    private int gia;
    private String moTa;

    public HangHoa(String tenHangHoa, int gia, String moTa) {
        this.tenHangHoa = tenHangHoa;
        this.gia = gia;
        this.moTa = moTa;
    }

    public int getGia() {
        return gia;
    }

}
